    private void jButton_gambarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_gambarActionPerformed
        String path = null;
        JFileChooser chooser = new JFileChooser();
        chooser.setCurrentDirectory(new File(System .getProperty("user.home")));

        FileNameExtensionFilter extension = new FileNameExtensionFilter("*Images", "jpg","png","jpeg");
        chooser.addChoosableFileFilter(extension);

        int fileState = chooser.showSaveDialog(null);

        if (fileState == JFileChooser.APPROVE_OPTION){

            File pilihGambar = chooser.getSelectedFile();
            path = pilihGambar.getAbsolutePath();
            jLabel_imagepacth.setText(path);
            ImageIcon img = new ImageIcon(path);
            Image im = img.getImage().getScaledInstance(jLabel_photo.getWidth(),jLabel_photo.getHeight(), Image.SCALE_SMOOTH);
            ImageIcon image = new ImageIcon(im);
            jLabel_photo.setIcon(image);
            imagePath = path;
        }
    }//GEN-LAST:event_jButton_gambarActionPerformed

