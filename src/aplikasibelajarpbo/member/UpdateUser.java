    public Date setTtl(String t){
        
        SimpleDateFormat dateFormat = new SimpleDateFormat("yyyy-MM-dd");
        
        SimpleDateFormat formatter = new SimpleDateFormat("MM/dd/yyyy");
        

        try {

            Date date = dateFormat.parse(t);
            formatter.format(date);
            System.out.println(date);
            System.out.println(formatter.format(date));
            return date;

        } catch (ParseException e) {
            e.printStackTrace();
            return null;
        }
        
    }
    public void setLabel(Member member){
        id = String.valueOf(member.getId());
        jTextField_username.setText(member.getUsername());
        jTextField_email.setText(member.getEmail());
        jTextField_nohp.setText(member.getNoTelp());
        jTextField_kota.setText(member.getKota());
        jPasswordField_pass1.setText(member.getPassword());
        jPasswordField_pass2.setText(member.getPassword());
        jXDatePicker_tgl_lahir.setDate(setTtl(member.getTtl()));
        
        if(member.getGender().equals("Pria")){
            jRadioButton_pria.setSelected(true);
            jRadioButton_wanita.setSelected(false);
        }else{
            jRadioButton_wanita.setSelected(true);
            jRadioButton_pria.setSelected(false);
        }
        if(member.getPhoto()!= null){
            jLabel_photo.setIcon(resizeImage(member.getPhoto()));
        }else{
            jLabel_photo.setIcon(new ImageIcon(new javax.swing.ImageIcon(getClass().getResource("/aplikasibelajarpbo/images/nopoto.jpg")).getImage().getScaledInstance(jLabel_photo.getWidth(),jLabel_photo.getHeight(), Image.SCALE_SMOOTH)));
        }
        
    }
    
     private ImageIcon resizeImage(byte[] photo){
        ImageIcon imageIcon = new ImageIcon(photo);
        Image img = imageIcon.getImage().getScaledInstance(jLabel_photo.getWidth(),jLabel_photo.getHeight(),Image.SCALE_SMOOTH);
        ImageIcon icon = new ImageIcon(img); 
        return icon;
    }

     public Boolean verifyField(){
        String username = jTextField_username.getText();
        String email = jTextField_email.getText();
        String nohp = jTextField_nohp.getText();
        String kota = jTextField_kota.getText();
        String ttl =String.valueOf(jXDatePicker_tgl_lahir.getDate());
        String pass1 = String.valueOf(jPasswordField_pass1.getPassword());
        String pass2 = String.valueOf(jPasswordField_pass2.getPassword());
        
        if (username.trim().equals("") || email.trim().equals("") || nohp.trim().equals("") || kota.trim().equals("") ||
                ttl.trim().equals("") || pass1.trim().equals("") ||pass2.trim().equals("")){
            JOptionPane.showMessageDialog(null, "Field masih ada yg kosong", "Field Kosong",2);
            return false;
            
        }
        else if(!pass1.equals(pass2)){
            JOptionPane.showMessageDialog(null, "Passoword berbeda", "Konfirmasi Password", 2);
            return false;
        }
        else{
            return true;
        }
    }
