    private void jButton_registrasiActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_registrasiActionPerformed
        String username = jTextField_username.getText();
        String email = jTextField_email.getText();
        String nohp = jTextField_nohp.getText();
        String kota = jTextField_kota.getText();
        String pass1 = String.valueOf(jPasswordField_pass1.getPassword());
        String pass2 = String.valueOf(jPasswordField_pass2.getPassword());
        String gender = "Pria";

        SimpleDateFormat dateFormat = new SimpleDateFormat("yyyy-MM-dd");
        String date = dateFormat.format(jXDatePicker_tgl_lahir.getDate());

        if(jRadioButton_wanita.isSelected()){
            gender = "Wanita";
        }

        if (verifyField()){
            DatabaseImpl database = new DatabaseImpl();
            Member member = database.update(username, pass1, email, nohp, date, kota, gender, imagePath, id);
            if (member != null){
                JOptionPane.showMessageDialog(null, "Update Berhasil !!");
                MenuMember.mem = member;
                this.dispose();
            }else{
                JOptionPane.showMessageDialog(null, "Update Gagal !!");
            }

        }
    }//GEN-LAST:event_jButton_registrasiActionPerformed

